<template>
  <div :class="['form', form.type]">
    <div class="img-box">
      <a @click="playVoice">
        <img :src="assetsDomain + form.image" alt="">
      </a>
    </div>
    <div class="content">
      <div class="sentences"
        :id="'sentences' + index"
        v-for="(item, index) in sentences"
        :key="item.uuid + index">
        <a @click="playSentence(item)"><i></i></a>
        <span class="line"></span>
        <span class="sentence">{{ item.sentence }}</span>
      </div>
    </div>
  </div>
</template>

<script>
import _ from 'lodash'
import { mapState } from 'vuex'

export default {
  props: ['form', 'slideForms'],
  data () {
    return {
      isPlay: false,
      sentences: []
    }
  },
  computed: {
    ...mapState({
      assetsDomain: state => state.course.assetsDomain
    })
  },
  created () {
    this.$on('init', () => {
      // let that = this
      console.log('imgToSentence init')
      console.log(this.form, this.slideForms)
      this.sentences = this.getSentences()
    })
  },
  mounted () {
    console.log(this.form, this.slideForms)
  },
  methods: {
    playVoice () {
      let audio = new Audio()
      if (!this.isPlay) {
        audio.src = this.assetsDomain + this.form.sound
        audio.oncanplay = () => {
          audio.play()
          this.isPlay = true
        }
        audio.onended = () => {
          this.isPlay = false
        }
      } else {
        audio.pause()
        this.isPlay = false
      }
    },
    // 播放句子
    playSentence (item) {
      let audio = new Audio()
      if (!this.isPlay) {
        audio.src = this.assetsDomain + item.sound
        audio.oncanplay = () => {
          audio.play()
          this.isPlay = true
        }
        audio.onended = () => {
          this.isPlay = false
        }
      } else {
        audio.pause()
        this.isPlay = false
      }
    },
    // 从句子数组中随机抽出3个句子进行选择（包含正确的句子）
    getSentences () {
      let answer = this.form
      let arr = [answer]
      let options = this.slideForms.filter((val) => {
        return val.sentence !== answer.sentence
      })
      console.log('options', options)
      arr = arr.concat(_.sampleSize(options, 2))
      arr = _.shuffle(arr)
      console.log('arr', arr)
      return arr
    }
  }
}
</script>

