<template>
  <div class="question-container">
    <div class="question-content">
      <div class="top-bar">
        <el-button style="outline:none;" type="primary" class="btnAdd" @click="addType()">添加</el-button>
      </div>
      <el-table
        :data="showTableData"
        style="width: 100%;">
        <el-table-column
          label="序号"
          width="80"
          align="center">
          <template slot-scope="scope">
            <span>{{scope.$index+(pageRequest.pageNum - 1) * pageRequest.pageSize + 1}}</span>
          </template>
        </el-table-column>
        <el-table-column
          width="200"
          label="类型">
          <template slot-scope="scope">
            <el-tooltip :content="scope.row.type" width="300" placement="top" effect="light">
              <span>{{ scope.row.type }}</span>
            </el-tooltip>
          </template>
        </el-table-column>
        <el-table-column
          width="200"
          label="名称">
          <template slot-scope="scope">
            <el-tooltip :content="scope.row.name" width="300" placement="top" effect="light">
              <span>{{ scope.row.name }}</span>
            </el-tooltip>
          </template>
        </el-table-column>
        <el-table-column
          width="300"
          label="描述">
          <template slot-scope="scope">
            <el-tooltip :content="scope.row.desc" width="300" placement="top" effect="light">
              <span>{{ scope.row.desc }}</span>
            </el-tooltip>
          </template>
        </el-table-column>
        <el-table-column
          fixed="right"
          label="操作">
          <template slot-scope="scope">
            <el-button type="danger" size="small" @click="delType(scope.row.type)">删除</el-button>
          </template>
        </el-table-column>
      </el-table>
      <div class="pagination-box">
        <el-pagination
          background layout="prev, pager, next"
          :current-page="pageRequest.pageNum"
          :page-size="pageRequest.pageSize"
          @size-change="handleSizeChange"
          @current-change="handleCurrentChange"
          :total="contentTypeList.length"
          >
        </el-pagination>
      </div>
    </div>
    <edit-comp ref="contentTypeEdit" @addContentType="initData"/>
  </div>
</template>

<script>
import { mapState, mapActions } from 'vuex'
import {
  delContentType
} from '@/api/course'
import EditComp from './edit'

export default {
  data () {
    return {
      showTableData: [],
      // 分页信息
      pageRequest: {
        pageNum: 1,
        pageSize: 5
      }
    }
  },
  created () {
    this.initData()
  },
  components: {
    EditComp
  },
  computed: {
    ...mapState({
      contentTypeList: state => state.course.contentTypeList
    })
  },
  methods: {
    ...mapActions({
      getContentTypeList: 'course/getContentTypeList'
    }),
    initData () {
      this.getContentTypeList({ pageNo: 0, pageSize: 0 })
      console.log('initData')
      this.handleCurrentChange(this.pageRequest.pageNum)
    },
    handleSizeChange (val) {
      console.log(`每页 ${val} 条`)
      this.pageRequest.pageSize = val
    },
    handleCurrentChange (val) {
      console.log(`当前页: ${val}`)
      this.pageRequest.pageNum = val
      let starNum = (val - 1) * this.pageRequest.pageSize
      let endNum = val * this.pageRequest.pageSize
      this.showTableData = this.contentTypeList.slice(starNum, endNum)
    },
    addType () {
      this.$refs.contentTypeEdit.show()
    },
    delType (type) {
      console.log(type)
      this.$confirm('此操作将永久删除该类型, 是否继续?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        this.$message({
          type: 'success',
          message: '删除成功!'
        })
        delContentType({ type: type }).then(res => {
          if (res.success) {
            this.initData()
          }
        })
      }).catch(() => {
        this.$message({
          type: 'info',
          message: '已取消删除'
        })
      })
    }
  }
}
</script>

